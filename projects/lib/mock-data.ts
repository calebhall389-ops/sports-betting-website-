export function americanToDecimal(odds: number): number {
  if (odds > 0) return odds / 100 + 1;
  return 100 / Math.abs(odds) + 1;
}

export function americanToImplied(odds: number): number {
  if (odds > 0) return 100 / (odds + 100);
  return Math.abs(odds) / (Math.abs(odds) + 100);
}

export function impliedToAmerican(prob: number): number {
  if (prob >= 0.5) return Math.round(-(prob / (1 - prob)) * 100);
  return Math.round(((1 - prob) / prob) * 100);
}

export function calculateEV(modelProb: number, americanOdds: number): number {
  const decimal = americanToDecimal(americanOdds);
  return modelProb * (decimal - 1) - (1 - modelProb);
}

export function calculateCLV(placedLine: number, closingLine: number): number {
  const placedImplied = americanToImplied(placedLine);
  const closingImplied = americanToImplied(closingLine);
  return closingImplied - placedImplied;
}

export function formatOdds(odds: number): string {
  return odds > 0 ? `+${odds}` : `${odds}`;
}

export function formatCLV(clv: number): string {
  const pct = (clv * 100).toFixed(2);
  return clv > 0 ? `+${pct}%` : `${pct}%`;
}
